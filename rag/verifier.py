# ----------------------------------------------------------------------
# Импорты
# ----------------------------------------------------------------------
import re  # для работы с регулярными выражениями (поиск чисел)
from typing import List, Tuple  # аннотации типов: список, кортеж
from langchain_core.documents import Document  # тип для представления фрагмента текста с метаданными


# ----------------------------------------------------------------------
# Вспомогательная функция: извлечение чисел из текста
# ----------------------------------------------------------------------
def extract_numbers(text: str) -> List[float]:
    """
    Извлекает из строки все числа (целые и дробные).
    Возвращает список чисел типа float.

    Особенности:
    - заменяет запятые на точки (для поддержки десятичных дробей вида "304,7")
    - ищет паттерн: граница слова, затем цифры, возможно точка и ещё цифры, затем граница слова
    """
    if not text:  # защита от пустой строки или None
        return []
    # Приводим десятичный разделитель к единому формату (точка)
    text = text.replace(',', '.')
    # Регулярное выражение: \b - граница слова, \d+ - одна или более цифр,
    # (?:\.\d+)? - необязательная группа: точка и ещё цифры
    numbers = re.findall(r'\b\d+(?:\.\d+)?\b', text)
    # Преобразуем строки в числа с плавающей точкой
    return [float(n) for n in numbers]


# ----------------------------------------------------------------------
# Основная функция верификации ответа LLM
# ----------------------------------------------------------------------
def verify_answer(question: str, answer: str, fragments: List[Document]) -> Tuple[bool, str]:
    """
    Проверяет, что ответ LLM корректен и безопасен для показа пользователю.

    Критерии:
    1. В ответе присутствует ссылка на источник (строка "Источник: ...").
    2. Все числа, упомянутые в ответе, действительно встречаются в тексте
       исходных фрагментов (с погрешностью 0.01).

    Возвращает:
        (True, reason)   — верификация пройдена
        (False, reason)  — верификация не пройдена, reason объясняет причину
    """

    # ------------------------------------------------------------------
    # Защита от некорректных значений
    # ------------------------------------------------------------------
    if answer is None:
        return False, "Ответ отсутствует (None)"
    if not isinstance(answer, str):
        return False, "Ответ не является строкой"

    # Склеиваем текст всех фрагментов в одну строку (для поиска чисел)
    context_text = "\n".join([f.page_content for f in fragments])

    # ------------------------------------------------------------------
    # 1. Проверка наличия ссылки на источник
    # ------------------------------------------------------------------
    # Наличие фразы "Источник:" — минимальное требование для отслеживаемости
    if "Источник:" not in answer:
        return False, "В ответе отсутствует ссылка на источник (формат 'Источник: \"Название статьи\"')."

    # ------------------------------------------------------------------
    # 2. Проверка чисел
    # ------------------------------------------------------------------
    # Извлекаем все числа из ответа LLM
    numbers_in_answer = extract_numbers(answer)
    if numbers_in_answer:
        # Извлекаем все числа из исходных фрагментов
        numbers_in_context = extract_numbers(context_text)
        missing_numbers = []
        # Для каждого числа из ответа проверяем, есть ли оно в контексте
        for num in numbers_in_answer:
            # Используем погрешность 0.01 (сравнение float)
            if not any(abs(num - ctx_num) < 0.01 for ctx_num in numbers_in_context):
                missing_numbers.append(num)
        if missing_numbers:
            # Если хотя бы одно число из ответа не найдено в источниках, отклоняем ответ
            return False, f"Число(а) {missing_numbers} не найдены в источниках."

    # Если все проверки пройдены
    return True, "Верификация пройдена"